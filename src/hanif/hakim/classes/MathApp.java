package hanif.hakim.classes;

public class MathApp {
    public static void main(String[] args) {
        /**
         * It's possible to run Math Operation in Java
         * 'Math' class is very useful to run math operation in java
         * such as compare high to low value, do square, running logarithm
         * all method in 'Math' class are static.
         */

        var min = Math.min(1000,2000);
        var max = Math.max(1000,2000);
        System.out.println(max);
        System.out.println(min);
        System.out.println(Math.PI);
        System.out.println(Math.cos(1));


    }
}
